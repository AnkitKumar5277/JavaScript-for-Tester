public class DataTypeDemo {
    public static void main(String[] args) {
        
        // 1. Integer: Used for whole numbers (counts, ages, IDs)
        int a = 10;           
        
        // 2. Double: Used for precise decimal values (prices, coordinates)
        double price = 99.5; 
        
        // 3. Boolean: Used for conditional logic (pass/fail, visible/hidden)
        boolean result = true; 
        
        // 4. Char: Used for single characters (must use single quotes '')
        char grade = 'A';    
        
        // 5. String: Used for sequences of text (must use double quotes "")
        String name = "Ankit"; 

        // Printing the values to the console
        System.out.println("User Name: " + name);
        System.out.println("Grade: " + grade);
        System.out.println("Item Price: " + price);
        System.out.println("Is Test Passed? " + result);
        System.out.println("Quantity: " + a);
    }
}
